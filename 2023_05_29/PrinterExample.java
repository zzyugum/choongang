package class0528.pro;

public class PrinterExample {

	int a;
	boolean b;
	double c;
	String d;
	
	void println(int a) {
		System.out.println(a);
	} 


	void println(String d) {
		System.out.println(d);
	}

	void println(double c) {
		System.out.println(c);
	}

	void println(boolean b) {
		System.out.println(b);
	}

}

